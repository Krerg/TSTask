function openDriverInfo() {
    $('#driverInfo').css('display','block')
    $('#deliverItem').css('display','none')
    $('#changeStatus').css('display','none')
}
function openDeliverItem() {
    $('#driverInfo').css('display','none')
    $('#deliverItem').css('display','block')
    $('#changeStatus').css('display','none')
}
function openChangeStatus() {
    $('#driverInfo').css('display','none')
    $('#deliverItem').css('display','none')
    $('#changeStatus').css('display','block')
}